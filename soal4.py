n = int(input("Enter a number: ")) # Meminta pengguna untuk memasukkan sebuah angka.

# Loop dari 1 hingga n
for i in range(1, n + 1):
        # i adalah bilangan ganjil
        if i % 2 != 0: # Ini adalah cara untuk memeriksa bilangan (genap/ganjil) dari sebuah angka.
            print(i) # Cetak bilangan ganjil

